# Fix up a turtlebot scan so Karto doesn't complain

import roslib; roslib.load_manifest('laser_slam')
import rospy
from sensor_msgs.msg import LaserScan

def scan_callback(scan, pub):
    scan.ranges = scan.ranges + (0.0,)
    pub.publish(scan)

def main():
    rospy.init_node('fix_scans')
    pub = rospy.Publisher('base_scan', LaserScan)
    sub = rospy.Subscriber('scan', LaserScan, scan_callback, pub)
    rospy.spin()


if __name__ == "__main__":
    main()
